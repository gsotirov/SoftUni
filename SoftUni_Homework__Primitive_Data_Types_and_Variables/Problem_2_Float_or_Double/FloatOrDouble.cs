using System;

namespace Problem_2_Float_or_Double
{
	class FloatOrDouble
	{
		public static void Main ()
		{
			double number1 = 34.567839023;
			float number2 = 12.345f;
			double number3 = 8923.1234857;
			float number4 = 3456.091f;

			Console.WriteLine ("{0} -> double\n{1} -> float\n{2} -> double\n{3} -> float", number1, number2, number3, number4);
		}
	}
}
